
// Array Traversing

import java.util.Arrays;

class Traversing1{
    public static void main(String[] args){
        int[] arr = {1 , 3 , 5 , 7};
        System.out.println();
        for(int i = 0; i < 4; i++){
            System.out.print(arr[i] + " ");
        }
        System.out.println("\n");     
    }
}

